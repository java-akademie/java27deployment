package ch.jmildner.hallo;

public class Rechner
{
	public static int add(int a, int b)
	{
		return a + b;
	}


	public static double div(double a, double b)
	{
		return a / b;
	}


	public static int mult(int a, int b)
	{
		return a * b;
	}


	public static int sub(int a, int b)
	{
		return a - b;
	}
}
